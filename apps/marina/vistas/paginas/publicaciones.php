 <h4>¿Que es un Portal Web?</h4>
 <span>Es un ecosistema virtual, donde se puede interactuar con personas en linea o sistemas de información.</span>
 
 <span>En general, los portales tienen estos elementos.</span>
 <ul>
	<li>Páginas Web</li>
	<li>Un menu de navegación</li>
	<li>Información de diferentes fuentes.</li>
	<li>Presentación personalizada de la información.</li>
	<li>Servicios de uso común (pagos en linea, mapas).</li>
	<li>Servicios a la medida.</li>	
 </ul>
 
 <h4>Clasificación de portales web</h4>
 <ul>
	<li>Minisitio</li>
	<li>Website básico, estático/animado</li>
	<li>Website dinámico (CMS)</li>
	<li>Tienda en linea</li>
	<li>Webste con aplicaciones específicas</li>
 </ul>
 
 <h4>Beneficios y ventajas de tener un portal web</h4>
 <ul>
	<li>Mejora la credibilidad de una marca.</li>
	<li>Imagen y prestigio</li>
	<li>Publicidad constante, 24 horas al día, 7 días a la semana, 365 días al año</li>
	<li>Presencia a nivel local, nacional y mundial</li>
	<li>Crecimiento en su cartera de de clientes y ventas</li>	
	<li>Mejora en la comunicación bidireccional con los clientes</li>
 </ul>
 
